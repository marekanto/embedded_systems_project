int8_t i2c_reg_write(uint8_t i2c_addr, uint8_t reg_addr, uint8_t *reg_data, uint16_t length) {

	int8_t result = HAL_I2C_Master_Sequential_Transmit_DMA(&hi2c1, i2c_addr, reg_data, length, HAL_MAX_DELAY);
	return result;
}
int8_t i2c_reg_read(uint8_t i2c_addr, uint8_t reg_addr, uint8_t *reg_data, uint16_t length) {
	uint8_t tx_buff[1];

	tx_buff[0] = reg_addr;
	HAL_I2C_Master_Transmit(&hi2c1, i2c_addr, &tx_buff[0], 1, HAL_MAX_DELAY);
	int8_t result = HAL_I2C_Master_Receive(&hi2c1, i2c_addr + 1, reg_data, length, HAL_MAX_DELAY);
	return result;
}

